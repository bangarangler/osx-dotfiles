---@type MappingsTable
local M = {}

M.general = {
  n = {
    [";"] = { ":", "enter command mode", opts = { nowait = true } },
    -- Toggle Transparency
    ["<leader>tt"] = {
      function()
        require("base46").toggle_transparency()
      end,
      "Toggle transparency",
    },
    ["<C-e>"] = { "3<C-e>", "Scroll Down Quickly" },
    ["<C-y>"] = { "3<C-y>", "Scroll Up Quickly" },
    ["jk"] = { "<esc>", "Exit Insert Mode" },
    ["<leader><leader>l"] = { ":noh<CR><C-L>" },

    --  format with conform
    ["<leader>fm"] = {
      function()
        require("conform").format()
      end,
      "formatting",
    },
  },
  v = {
    [">"] = { ">gv", "indent" },
  },
}

